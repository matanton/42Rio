#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*p;
	size_t	s_len;

	i = 0;
	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (s_len - start >= len)
		p = malloc(len + 1);
	else
		p = malloc((s_len - start) + 1);
	if (!p)
		return (NULL);
	ft_strlcpy(p, s + start, (len + 1));
	return (p);
}
